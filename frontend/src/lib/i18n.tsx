const dictionaries = {
  "en-US": enUS,
  "zh-CN": zhCN,
} as const;

type TranslationKey = keyof typeof enUS;

export function useI18n() {
  const {
    preferences: { locale },
  } = useAppPreferences();

  return {
    locale,
    t: (key: TranslationKey, values?: Record<string, string | number>) =>
      translate(locale, key, values),
  };
}

export function translate(
  locale: AppLocale,
  key: TranslationKey,
  values?: Record<string, string | number>,
) {
  const template = dictionaries[locale][key] ?? enUS[key];

  if (!values) {
    return template;
  }

  return template.replace(/\{(\w+)\}/g, (_match, token) => {
    return String(values[token] ?? "");
  });
}
